    raw_score_apy DOUBLE PRECISION,
    raw_score_pool_extra_lamports DOUBLE PRECISION,
    raw_score_city_concentration DOUBLE PRECISION,
    raw_score_country_concentration DOUBLE PRECISION,
    normalized_score_skip_rate DOUBLE PRECISION,
    normalized_score_prior_skip_rate DOUBLE PRECISION,
    normalized_score_subsequent_skip_rate DOUBLE PRECISION,
    normalized_score_cu DOUBLE PRECISION,
    normalized_score_latency DOUBLE PRECISION,
    normalized_score_llv DOUBLE PRECISION,
    normalized_score_cv DOUBLE PRECISION,
    normalized_score_vote_inclusion DOUBLE PRECISION,
    normalized_score_apy DOUBLE PRECISION,
    normalized_score_pool_extra_lamports DOUBLE PRECISION,
    normalized_score_city_concentration DOUBLE PRECISION,
    normalized_score_country_concentration DOUBLE PRECISION,
    pool_stake_active BIGINT,
    pool_stake_activating BIGINT,
    pool_stake_deactivating BIGINT,
    noneligibility_reasons JSONB,
    PRIMARY KEY (epoch, vote_account_pubkey)
