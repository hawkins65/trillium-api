    epoch INTEGER NOT NULL,
    vote_account_pubkey VARCHAR(44) NOT NULL,
    total_score DOUBLE PRECISION,
    name VARCHAR(100),
    icon_url TEXT,
    details TEXT,
    website_url TEXT,
    city VARCHAR(100),
    country CHAR(2),
    stake_active BIGINT,
    stake_activating BIGINT,
    stake_deactivating BIGINT,
    target_pool_stake BIGINT,
    raw_score_skip_rate DOUBLE PRECISION,
    raw_score_prior_skip_rate DOUBLE PRECISION,
    raw_score_subsequent_skip_rate DOUBLE PRECISION,
    raw_score_cu DOUBLE PRECISION,
    raw_score_latency DOUBLE PRECISION,
    raw_score_llv DOUBLE PRECISION,
    raw_score_cv DOUBLE PRECISION,
